package day12switchincrementdecrementloops;

public class Switch01 {
    public static void main(String[] args) {

//ex:ay numarasi verildiginde,ayin ismini consol a yazdiran code u yazdiriniz.

       int monthNum=3;
       switch(monthNum){
           case 1:
               System.out.println("January");
               break;
           case 2:
               System.out.println("February");
               break;
           case 3:
               System.out.println("March");
               break;
           case 4:
               System.out.println("April");
               break;
           case 5:
               System.out.println("May");
               break;
           case 6:
               System.out.println("Juni");
               break;
           case 7:
               System.out.println("July");
               break;
           case 8:
               System.out.println("August");
               break;
           case 9:
               System.out.println("September");
               break;
           case 10:
               System.out.println("October");
               break;
           case 11:
               System.out.println("November");
               break;
           case 12:
               System.out.println("December");
               break;
               default :
                   System.out.println("Invalid month number...");
  //ex:ay numarasi verildiginde,numarasi verilen ay in ismini
  //december e kadarki tum aylarin  consol a yazdiran code u yazdiriniz.


       }

    }
}
